// 🧠 核心翻译器：增强版，支持提取四化解读
export class VCoreInterpreter {
  // 1. 获取主星基调解读
  static getBaseInterpretation(mainStars: string[], palaceName: string): PalaceInterpretation {
    let key = "通用_兜底";
    
    // 宽泛匹配，后续可扩展精准匹配
    if (mainStars.includes("紫微")) {
      key = `紫微_${palaceName}宫`;
    } 
    // ... 后续补充其他星系的判断逻辑 ...
    
    if (PALACE_INTERPRETATION_DICT[key]) {
      return PALACE_INTERPRETATION_DICT[key];
    }

    return {
      core_conclusion: `您的【${palaceName}宫】由 ${mainStars.length > 0 ? mainStars.join('') : '无主星'} 坐守。此处星曜组合较为复杂，代表了您在此领域的独特宿命与轨迹。`,
      advantage: ["在相关领域具有独特的潜能，等待挖掘。"],
      shortcoming: ["可能会面临一些隐性的情绪内耗或外界压力。"],
      scene_tips: "（正在等待大师录入此星系的深度解析剧本...）"
    };
  }

  // 2. 🚀 新增：获取四化动态干预解读
  static getMutagenInterpretation(mutagens: string[], palaceName: string): MutagenInterpretation[] {
    const interpretations: MutagenInterpretation[] = [];
    mutagens.forEach(mutagen => {
        // mutagen 可能是 "禄", "权", "科", "忌"
        const fullMutagenName = `化${mutagen}`; 
        const palaceDict = MUTAGEN_INTERPRETATION_DICT[fullMutagenName];
        
        if (palaceDict && palaceDict[`${palaceName}宫`]) {
            interpretations.push(palaceDict[`${palaceName}宫`]);
        }
    });
    return interpretations;
  }

  /**
   * 🚀 获取八吉星插件解读
   * @param minorStars 当前宫位的吉星列表
   * @param palaceName 宫位名称 (如 "命", "兄弟")
   */
  static getAuspiciousInterpretation(minorStars: string[], palaceName: string): MinorStarInterpretation[] {
    const results: MinorStarInterpretation[] = [];
    const processedPairs = new Set<string>(); // 防止左右、魁钺、昌曲成对出现时重复输出

    minorStars.forEach(star => {
      const dict = AUSPICIOUS_INTERPRETATION_DICT[star];
      if (dict && dict[`${palaceName}宫`]) {
        // 成对处理逻辑：如果已经处理过同组星曜，则跳过（因为描述是共用的）
        let pairKey = "";
        if (["左辅", "右弼"].includes(star)) pairKey = "左右";
        if (["天魁", "天钺"].includes(star)) pairKey = "魁钺";
        if (["文昌", "文曲"].includes(star)) pairKey = "昌曲";

        if (pairKey && processedPairs.has(pairKey)) return;
        if (pairKey) processedPairs.add(pairKey);

        results.push(dict[`${palaceName}宫`]);
      }
    });

    return results;
  }

  // 4. 🚀 升级版：获取六煞星精准宫位解读
  static getMaleficInterpretation(minorStars: string[], palaceName: string): MinorStarInterpretation[] {
    const interpretations: MinorStarInterpretation[] = [];
    
    minorStars.forEach(star => {
      const starDict = MALEFIC_INTERPRETATION_DICT[star];
      if (starDict) {
        // 精准匹配对应的宫位解读
        if (starDict[`${palaceName}宫`]) {
          interpretations.push(starDict[`${palaceName}宫`]);
        } else {
          // 如果未配置该宫位，则抓取命宫作为通用特征
          interpretations.push(starDict["命宫"]); 
        }
      }
    });
    return interpretations;
  }
}
